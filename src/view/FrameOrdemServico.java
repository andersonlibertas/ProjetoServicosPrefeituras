    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String data = txtData.getText();
        String validade =  txtValidade.getText();
        String setor = txtSetor.getText();
        String secretaria = txtSecretaria.getText();
        String observacao = txtSetor.getText();
        String tarefa = txtTarefa.getText();
        String situacao = txtSituacao.getText();
        
        Tarefa t = new Tarefa();
        t.setData(data);
        t.setObservacao(observacao);
        t.setSecretaria(secretaria);
        t.setTarefa(tarefa);
        t.setValidade(validade);
        t.setSetor(setor);
        t.setSituacao(situacao);
        
        
        
        TarefaDAO tDAO = new TarefaDAO();
   
        tDAO.addTarefa(t);
 
        txtData.setText("");
        txtValidade.setText("");
        txtSetor.setText("");
        txtTarefa.setText("");
        txtObservacao.setText("");
        txtSituacao.setText("");
        txtCod.setText("");
        
        JOptionPane.showMessageDialog(this, "Ordem de serviço inserida com sucesso!");
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

