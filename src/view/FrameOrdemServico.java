    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int cod =  Integer.parseInt(txtCod.getText());
        String data = txtData.getText();
        String validade =  txtValidade.getText();
        String setor = txtSetor.getText();
        String secretaria = txtSecretaria.getText();
        String tarefa = txtTarefa.getText();
        String observacao = txtObservacao.getText();
        String situacao = txtSituacao.getText();
        
        Tarefa t = new Tarefa();
        
        t.setCodigo(cod);
        t.setData(data);
        t.setObservacao(observacao);
        t.setSecretaria(secretaria);
        t.setSetor(setor);
        t.setSituacao(situacao);
        t.setTarefa(tarefa);
        t.setValidade(validade);
                
                
                
                
                
        TarefaDAO fDao = new TarefaDAO();
        fDao.updateTarefa(t);
        
        
        
        txtData.setText("");
        txtValidade.setText("");
        txtSetor.setText("");
        txtTarefa.setText("");
        txtObservacao.setText("");
        txtSituacao.setText("");
        txtCod.setText("");
        
        JOptionPane.showMessageDialog(this, "Ordem de serviço alterado com sucesso!");
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int cod=Integer.parseInt(txtCod.getText());
        TarefaDAO fDAO= new TarefaDAO();
        Tarefa t = fDAO.getUserById(cod);
      
        txtData.setText(t.getData());
        txtValidade.setText(t.getValidade());
        txtSetor.setText(t.getSetor());
        txtTarefa.setText(t.getTarefa());
        txtSecretaria.setText(t.getSecretaria());
        txtObservacao.setText(t.getObservacao());
        txtSituacao.setText(t.getSituacao());
        
        JOptionPane.showMessageDialog(this, "Dados retornados com sucesso!");
    }//GEN-LAST:event_btnBuscarActionPerformed

